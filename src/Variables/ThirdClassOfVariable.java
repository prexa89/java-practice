package Variables;

public class ThirdClassOfVariable {
    public static void main(String[] args) {
        int a =5;
        int b= 6;
        int c= 7;
        int mySum=a+b+c;
        int mySub=b-c;
        int mySub2=c-a;
        int myMulti=a*b*c;
        int myDivision=a/b;
        int myModulus=b%c;

        System.out.println(mySum);
        System.out.println(mySub);
        System.out.println(mySub2);
        System.out.println(myMulti);
        System.out.println(myDivision);
        System.out.println(myModulus);
    }
}
