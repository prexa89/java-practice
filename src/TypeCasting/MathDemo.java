package TypeCasting;

public class MathDemo {
    public static void main(String[] args) {
        System.out.println("maximum value:" + Math.max(100,90));
        System.out.println(("minimum value:"+Math.min(90,70)));//direct print
        System.out.println("random number:"+Math.random());
        System.out.println("PI"+Math.PI);
        System.out.println("square root:"+Math.sqrt(625));
        int myMin = Math.min(50,40);//declare variable and then print
        System.out.println(myMin);

    }
}
