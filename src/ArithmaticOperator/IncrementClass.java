package ArithmaticOperator;

public class IncrementClass {
    public static void main(String[] args) {
        int a=500;
        int b=200;
        int c=a++;//Increment operator
        int d=++b;
        System.out.println(a++);
        System.out.println(++a);
        System.out.println(c);
        System.out.println(d);

    }
}
