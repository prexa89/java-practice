package ArithmaticOperator;

public class DecrementOperator {
    public static void main(String[] args) {
        int a=150;
        int b=20;
        int c=a--;
        int d=--b; //Decrement Operator
        System.out.println(a--);
        System.out.println(--a);
        System.out.println(c);
        System.out.println(d);
    }
}
